"use client";

import {
  downloadWorkbenchFrameForceSummary,
  downloadWorkbenchFrameHotspotSummary,
  downloadWorkbenchPlaneHotspotSummary,
  downloadWorkbenchResultCsv,
  downloadWorkbenchResultJson,
} from "@/components/workbench/workbench-result-export-controller";
import {
  useWorkbenchResultWindowController,
} from "@/components/workbench/workbench-result-window-controller";
import {
  bindWorkbenchMainComposition,
  bindWorkbenchStudyResultDerived,
} from "@/components/workbench/workbench-root-bindings";
import { useWorkbenchSessionComposition } from "@/components/workbench/workbench-session-composition";
import { useWorkbenchStudyResultDerived } from "@/components/workbench/workbench-study-result-derived";
import { useWorkbenchMainComposition } from "@/components/workbench/workbench-main-composition";
import { resetActiveResult } from "@/components/workbench/workbench-file-helpers";
import {
  isAxialResult,
  isBeam1dResult,
  isElectrostaticPlaneQuad2dResult,
  isElectrostaticPlaneTriangle2dResult,
  isFrame2dResult,
  isHeatBar1dResult,
  isHeatPlaneQuad2dResult,
  isHeatPlaneTriangle2dResult,
  isSpring1dResult,
  isSpring2dResult,
  isSpring3dResult,
  isThermalBar1dResult,
  isThermalBeam1dResult,
  isThermalTruss2dResult,
  isThermalTruss3dResult,
  isTorsion1dResult,
  isTruss3dResult,
  isTrussResult,
  localMaterialLabel,
} from "@/components/workbench/workbench-result-helpers";
import { SECURITY_EVENT_WINDOW_MS } from "@/components/workbench/workbench-types";
import {
  fixed,
  formatMilliseconds,
  formatTime,
  serializeCurrentModel,
} from "@/lib/workbench/helpers";

export function useWorkbenchCoreComposition(props: Record<string, any>) {
  const { rootState } = props;

  const resultWindowMaxTotal = rootState.resultWindow
    ? Math.max(rootState.resultWindow.totalNodes, rootState.resultWindow.totalElements)
    : 0;

  const { handleCanvasStageScroll } = useWorkbenchResultWindowController({
    canvasStageRef: rootState.canvasStageRef,
    canvasViewportWidth: rootState.canvasViewportWidth,
    frontendRuntimeMode: rootState.frontendRuntimeMode,
    guards: {
      isAxialResult,
      isTrussResult,
      isHeatBar1dResult,
      isElectrostaticPlaneQuad2dResult,
      isElectrostaticPlaneTriangle2dResult,
      isHeatPlaneQuad2dResult,
      isHeatPlaneTriangle2dResult,
      isThermalBar1dResult,
      isThermalBeam1dResult,
      isThermalTruss2dResult,
      isThermalTruss3dResult,
      isTruss3dResult,
      isSpring1dResult,
      isSpring2dResult,
      isSpring3dResult,
      isBeam1dResult,
      isTorsion1dResult,
      isFrame2dResult,
    },
    jobId: rootState.job?.job_id ?? null,
    requestTimedOutLabel: rootState.t.requestTimedOut,
    result: rootState.result,
    resultWindow: rootState.resultWindow,
    resultWindowLimit: rootState.resultWindowLimit,
    renderStrategy: rootState.renderStrategy,
    resultWindowMaxTotal,
    resultWindowOffset: rootState.resultWindowOffset,
    setCanvasViewportWidth: rootState.setCanvasViewportWidth,
    setMessage: rootState.setMessage,
    setSystemAlerts: rootState.setSystemAlerts,
    setResultWindow: rootState.setResultWindow,
    setResultWindowLimit: rootState.setResultWindowLimit,
    setResultWindowOffset: rootState.setResultWindowOffset,
    studyKind: rootState.studyKind,
  });

  let mainCompositionRef: {
    recordHistory?: (action: string) => void;
    refreshResults?: () => Promise<void> | void;
  } | null = null;

  async function refreshResults() {
    await mainCompositionRef?.refreshResults?.();
  }

  function recordHistoryBridge(action: string) {
    mainCompositionRef?.recordHistory?.(action);
  }

  const sessionComposition = useWorkbenchSessionComposition({
    t: rootState.t,
    job: rootState.job,
    jobIsActive: rootState.jobIsActive,
    jobPollTokenRef: rootState.jobPollTokenRef,
    setJob: rootState.setJob,
    setMessage: rootState.setMessage,
    startTransition: rootState.startTransition,
    resultRecords: rootState.resultRecords,
    projects: rootState.projects,
    selectedProjectId: rootState.selectedProjectId,
    selectedModelId: rootState.selectedModelId,
    modelVersions: rootState.modelVersions,
    setSidebarSection: rootState.setSidebarSection,
    directMeshEndpointsText: rootState.directMeshEndpointsText,
    directMeshSelectionMode: rootState.directMeshSelectionMode,
    frontendRuntimeMode: rootState.frontendRuntimeMode,
    setHealth: rootState.setHealth,
    setModelVersions: rootState.setModelVersions,
    setProjects: rootState.setProjects,
    setProtocolAgents: rootState.setProtocolAgents,
    setRuntimeRecovery: rootState.setRuntimeRecovery,
    setSelectedModelId: rootState.setSelectedModelId,
    setSelectedProjectId: rootState.setSelectedProjectId,
    setSelectedVersionId: rootState.setSelectedVersionId,
    refreshResults,
    securityEventWindowMs: SECURITY_EVENT_WINDOW_MS,
    setLibraryTab: rootState.setLibraryTab,
    activeMaterial: rootState.activeMaterial,
    createProject: props.createProject,
    createModel: props.createModel,
    createModelVersion: props.createModelVersion,
    updateModelVersion: props.updateModelVersion,
    fetchModel: props.fetchModel,
    fetchModelVersion: props.fetchModelVersion,
    recordHistory: recordHistoryBridge,
    resetActiveResult: () => resetActiveResult(rootState.setResult, rootState.setJob),
    setLoadedModelName: rootState.setLoadedModelName,
    setStudyKind: rootState.setStudyKind,
    setAxialForm: rootState.setAxialForm,
    setHeatBarModel: rootState.setHeatBarModel,
    setHeatPlaneModel: rootState.setHeatPlaneModel,
    setThermalBarModel: rootState.setThermalBarModel,
    setThermalBeamModel: rootState.setThermalBeamModel,
    setThermalFrameModel: rootState.setThermalFrameModel,
    setThermalTrussModel: rootState.setThermalTrussModel,
    setThermalTruss3dModel: rootState.setThermalTruss3dModel,
    setSpringModel: rootState.setSpringModel,
    setSpring2dModel: rootState.setSpring2dModel,
    setSpring3dModel: rootState.setSpring3dModel,
    setTrussModel: rootState.setTrussModel,
    setTruss3dModel: rootState.setTruss3dModel,
    setPlaneModel: rootState.setPlaneModel,
    setFrameModel: rootState.setFrameModel,
    setBeamModel: rootState.setBeamModel,
    setTorsionModel: rootState.setTorsionModel,
    setPlaneResultField: rootState.setPlaneResultField,
    setParametric: rootState.setParametric,
    setActiveMaterial: rootState.setActiveMaterial,
    projectNameDraft: rootState.projectNameDraft,
    projectDescriptionDraft: rootState.projectDescriptionDraft,
    loadedModelName: rootState.loadedModelName,
    studyKind: rootState.studyKind,
    axialForm: rootState.axialForm,
    heatBarModel: rootState.heatBarModel,
    heatPlaneModel: rootState.heatPlaneModel,
    thermalBarModel: rootState.thermalBarModel,
    thermalBeamModel: rootState.thermalBeamModel,
    thermalFrameModel: rootState.thermalFrameModel,
    thermalTrussModel: rootState.thermalTrussModel,
    trussModel: rootState.trussModel,
    thermalTruss3dModel: rootState.thermalTruss3dModel,
    truss3dModel: rootState.truss3dModel,
    planeModel: rootState.planeModel,
    frameModel: rootState.frameModel,
    beamModel: rootState.beamModel,
    torsionModel: rootState.torsionModel,
    springModel: rootState.springModel,
    spring2dModel: rootState.spring2dModel,
    spring3dModel: rootState.spring3dModel,
    parametric: rootState.parametric,
    round: props.round,
    updateProject: props.updateProject,
    deleteProject: props.deleteProject,
    updateModel: props.updateModel,
    deleteModel: props.deleteModel,
    deleteModelVersion: props.deleteModelVersion,
    fetchModelVersions: props.fetchModelVersions,
    fetchJobStatus: props.fetchJobStatus,
    serializeCurrentModel: () =>
      serializeCurrentModel(
        rootState.studyKind,
        rootState.loadedModelName,
        rootState.activeMaterial,
        rootState.axialForm,
        rootState.heatBarModel,
        rootState.heatPlaneModel,
        rootState.thermalBarModel,
        rootState.thermalBeamModel,
        rootState.thermalFrameModel,
        rootState.thermalTrussModel,
        rootState.trussModel,
        rootState.thermalTruss3dModel,
        rootState.truss3dModel,
        rootState.planeModel,
        rootState.frameModel,
        rootState.beamModel,
        rootState.torsionModel,
        rootState.springModel,
        rootState.spring2dModel,
        rootState.spring3dModel,
        rootState.parametric,
        props.round,
      ),
  });

  const {
    jobHistoryController,
    adminSecurityState,
    workflowController,
    selectedAdminJob,
    selectedProjectModels,
    deferredProjectModels,
    deferredModelVersions,
    deferredJobHistory,
    deferredResultRecords,
    refreshHealth,
    refreshProjects,
    refreshSecurityEvents,
    refreshVersions,
    retryRuntimeRecovery,
    projectFlows,
  } = sessionComposition;

  const studyResultDerived = useWorkbenchStudyResultDerived({
    studyKind: rootState.studyKind,
    result: rootState.result,
    job: rootState.job,
    resultWindow: rootState.resultWindow,
    t: rootState.t,
    selectedNode: rootState.selectedNode,
    selectedElement: rootState.selectedElement,
    axialForm: rootState.axialForm,
    heatBarModel: rootState.heatBarModel,
    heatPlaneModel: rootState.heatPlaneModel,
    thermalBarModel: rootState.thermalBarModel,
    thermalBeamModel: rootState.thermalBeamModel,
    thermalFrameModel: rootState.thermalFrameModel,
    thermalTrussModel: rootState.thermalTrussModel,
    thermalTruss3dModel: rootState.thermalTruss3dModel,
    springModel: rootState.springModel,
    spring2dModel: rootState.spring2dModel,
    spring3dModel: rootState.spring3dModel,
    trussModel: rootState.trussModel,
    truss3dModel: rootState.truss3dModel,
    planeModel: rootState.planeModel,
    frameModel: rootState.frameModel,
    beamModel: rootState.beamModel,
    torsionModel: rootState.torsionModel,
    beamResultField: rootState.beamResultField,
    frameResultField: rootState.frameResultField,
    planeResultField: rootState.planeResultField,
    planeHotspotLimit: rootState.planeHotspotLimit,
    renderStrategy: rootState.renderStrategy,
    language: rootState.language,
    loadedModelName: rootState.loadedModelName,
    downloadTextFile: props.downloadTextFile,
    setMessage: rootState.setMessage,
    health: rootState.health,
    frontendRuntimeMode: rootState.frontendRuntimeMode,
    directMeshSelectionMode: rootState.directMeshSelectionMode,
    directMeshExecution: rootState.directMeshExecution,
    directMeshApiToken: rootState.directMeshApiToken,
    protocolAgents: rootState.protocolAgents,
    hiddenMaterials: rootState.hiddenMaterials,
    deferredJobHistory,
    deferredResultRecords,
    deferredProjectModels,
    deferredModelVersions,
    jobHistory: jobHistoryController.jobHistory,
    adminFilterProjectId: adminSecurityState.adminFilterProjectId,
    adminFilterModelVersionId: adminSecurityState.adminFilterModelVersionId,
    projects: rootState.projects,
    securityEventRecords: adminSecurityState.securityEventRecords,
    securityEventWindowFilter: adminSecurityState.securityEventWindowFilter,
    formatTime,
    formatMilliseconds,
    round: props.round,
    resultWindowLimit: rootState.resultWindowLimit,
    resultWindowMaxTotal,
    resultWindowOffset: rootState.resultWindowOffset,
    directMeshEndpointsText: rootState.directMeshEndpointsText,
    immersiveViewport: rootState.immersiveViewport,
    immersiveToolDrawerOpen: rootState.immersiveToolDrawerOpen,
    immersiveHelpDrawerOpen: rootState.immersiveHelpDrawerOpen,
    showShortcutHints: rootState.showShortcutHints,
  });

  const resultDerivedBindings = bindWorkbenchStudyResultDerived(studyResultDerived);

  const resultExportEffects = resultDerivedBindings.resultExportEffects;
  const downloadResultJson = () => downloadWorkbenchResultJson(resultExportEffects);
  const downloadResultCsv = () => downloadWorkbenchResultCsv(resultExportEffects);
  const downloadPlaneHotspotSummary = () => downloadWorkbenchPlaneHotspotSummary(resultExportEffects);
  const downloadFrameHotspotSummary = () => downloadWorkbenchFrameHotspotSummary(resultExportEffects);
  const downloadFrameForceSummary = () => downloadWorkbenchFrameForceSummary(resultExportEffects);

  const railItems = [
    { key: "model", label: rootState.t.rail.model, symbol: "M" },
    { key: "workflow", label: rootState.t.rail.workflow, symbol: "W" },
    { key: "library", label: rootState.t.rail.library, symbol: "H" },
    { key: "system", label: rootState.t.rail.system, symbol: "Y" },
  ];

  const mainComposition = useWorkbenchMainComposition({
    ...rootState,
    fixed,
    localMaterialLabel,
    workflowController,
    projectStorageController: projectFlows.projectStorageController,
    selectedProjectModels,
    adminSecurityState,
    deferredProjectModels,
    deferredModelVersions,
    deferredJobHistory,
    deferredResultRecords,
    resultWindowMaxTotal,
    handleCanvasStageScroll,
    refreshHealth,
    refreshProjects,
    refreshSecurityEvents,
    refreshVersions,
    retryRuntimeRecovery,
    runtimeRecovery: rootState.runtimeRecovery,
    refreshJobHistory: jobHistoryController.refreshJobHistory,
    cancelCurrentJob: jobHistoryController.cancelCurrentJob,
    selectedAdminJobId: jobHistoryController.selectedAdminJobId,
    setSelectedAdminJobId: jobHistoryController.setSelectedAdminJobId,
    selectedAdminJob,
    adminFilterProjectId: adminSecurityState.adminFilterProjectId,
    setAdminFilterProjectId: adminSecurityState.setAdminFilterProjectId,
    adminFilterModelVersionId: adminSecurityState.adminFilterModelVersionId,
    setAdminFilterModelVersionId: adminSecurityState.setAdminFilterModelVersionId,
    securityEventRecords: adminSecurityState.securityEventRecords,
    securityEventWindowFilter: adminSecurityState.securityEventWindowFilter,
    securityEventSourceFilter: adminSecurityState.securityEventSourceFilter,
    securityEventRiskFilter: adminSecurityState.securityEventRiskFilter,
    securityEventStatusFilter: adminSecurityState.securityEventStatusFilter,
    securityEventActionFilter: adminSecurityState.securityEventActionFilter,
    setSecurityEventWindowFilter: adminSecurityState.setSecurityEventWindowFilter,
    setSecurityEventSourceFilter: adminSecurityState.setSecurityEventSourceFilter,
    setSecurityEventRiskFilter: adminSecurityState.setSecurityEventRiskFilter,
    setSecurityEventStatusFilter: adminSecurityState.setSecurityEventStatusFilter,
    setSecurityEventActionFilter: adminSecurityState.setSecurityEventActionFilter,
    scriptRecordingMode: adminSecurityState.scriptRecordingMode,
    setScriptRecordingMode: adminSecurityState.setScriptRecordingMode,
    selectedAdminResultJobId: adminSecurityState.selectedAdminResultJobId,
    setSelectedAdminResultJobId: adminSecurityState.setSelectedAdminResultJobId,
    adminJobMessage: adminSecurityState.adminJobMessage,
    setAdminJobMessage: adminSecurityState.setAdminJobMessage,
    adminJobProjectId: adminSecurityState.adminJobProjectId,
    setAdminJobProjectId: adminSecurityState.setAdminJobProjectId,
    adminJobModelVersionId: adminSecurityState.adminJobModelVersionId,
    setAdminJobModelVersionId: adminSecurityState.setAdminJobModelVersionId,
    adminJobCaseId: adminSecurityState.adminJobCaseId,
    setAdminJobCaseId: adminSecurityState.setAdminJobCaseId,
    adminResultDraft: adminSecurityState.adminResultDraft,
    setAdminResultDraft: adminSecurityState.setAdminResultDraft,
    projectFlows,
    shellState: rootState.shellState,
    workspaceState: rootState.workspaceState,
    studyResultDerived,
    startTransition: rootState.startTransition,
    jobPollTokenRef: rootState.jobPollTokenRef,
    downloadTextFile: props.downloadTextFile,
    resetActiveResult: () => resetActiveResult(rootState.setResult, rootState.setJob),
    createProject: props.createProject,
    updateProject: props.updateProject,
    deleteProject: props.deleteProject,
    createModel: props.createModel,
    updateModel: props.updateModel,
    deleteModel: props.deleteModel,
    createModelVersion: props.createModelVersion,
    updateModelVersion: props.updateModelVersion,
    deleteModelVersion: props.deleteModelVersion,
    fetchResults: props.fetchResults,
    fetchJobStatus: props.fetchJobStatus,
    updateJobRecord: props.updateJobRecord,
    deleteJobRecord: props.deleteJobRecord,
    updateResultRecord: props.updateResultRecord,
    deleteResultRecord: props.deleteResultRecord,
    cancelJob: props.cancelJob,
    resolveTruss2dJobInput: props.resolveTruss2dJobInput,
    resolveTruss3dJobInput: props.resolveTruss3dJobInput,
    resolvePlaneQuad2dJobInput: props.resolvePlaneQuad2dJobInput,
    resolvePlaneTriangle2dJobInput: props.resolvePlaneTriangle2dJobInput,
    railItems,
    downloadResultJson,
    downloadResultCsv,
    downloadPlaneHotspotSummary,
    downloadFrameHotspotSummary,
    downloadFrameForceSummary,
  });
  mainCompositionRef = mainComposition;

  const mainBindings = bindWorkbenchMainComposition(mainComposition);

  return {
    sidebarMountProps: mainBindings.sidebarMountProps,
    mainShellMountProps: mainBindings.mainShellMountProps,
  };
}
