"use client";

import { buildWorkbenchStudyFlags } from "@/components/workbench/workbench-admin-context";
import { buildWorkbenchActiveResultState } from "@/components/workbench/workbench-active-result-state";
import { humanizeSolverFailure } from "@/components/workbench/workbench-copy";
import { buildWorkbenchResultDerivedData } from "@/components/workbench/workbench-result-derived";
import { buildWorkbenchSidebarDerived } from "@/components/workbench/workbench-sidebar-derived";
import {
  heartbeatStatus,
  heartbeatTone,
  isAxialResult,
  isBeam1dResult,
  isFrame2dResult,
  isHeatBar1dResult,
  isHeatPlaneQuad2dResult,
  isHeatPlaneTriangle2dResult,
  isPlaneResult,
  isSpring1dResult,
  isSpring2dResult,
  isSpring3dResult,
  isThermalBar1dResult,
  isThermalBeam1dResult,
  isThermalFrame2dResult,
  isThermalTruss2dResult,
  isThermalTruss3dResult,
  isTorsion1dResult,
  isTruss3dResult,
  isTrussResult,
} from "@/components/workbench/workbench-result-helpers";
import {
  resolveWorkbenchCurrentMaterials,
  useWorkbenchVisualDerived,
} from "@/components/workbench/workbench-visual-derived";

export function useWorkbenchStudyResultDerived(props: Record<string, any>) {
  const studyFlags = buildWorkbenchStudyFlags(props.studyKind);

  const activeResultState = buildWorkbenchActiveResultState({
    result: props.result,
    studyKind: props.studyKind,
    job: props.job,
    resultWindow: props.resultWindow,
    t: props.t,
    selectedNode: props.selectedNode,
    axialForm: props.axialForm,
    heatBarModel: props.heatBarModel,
    heatPlaneModel: props.heatPlaneModel,
    thermalBarModel: props.thermalBarModel,
    thermalBeamModel: props.thermalBeamModel,
    thermalFrameModel: props.thermalFrameModel,
    thermalTrussModel: props.thermalTrussModel,
    thermalTruss3dModel: props.thermalTruss3dModel,
    springModel: props.springModel,
    spring2dModel: props.spring2dModel,
    spring3dModel: props.spring3dModel,
    trussModel: props.trussModel,
    truss3dModel: props.truss3dModel,
    planeModel: props.planeModel,
    frameModel: props.frameModel,
    beamModel: props.beamModel,
    torsionModel: props.torsionModel,
    isAxial: studyFlags.isAxial,
    isHeatBar: studyFlags.isHeatBar,
    isHeatPlaneTriangle: studyFlags.isHeatPlaneTriangle,
    isHeatPlaneQuad: studyFlags.isHeatPlaneQuad,
    isHeatPlane: studyFlags.isHeatPlane,
    isThermalBar: studyFlags.isThermalBar,
    isThermalBeam: studyFlags.isThermalBeam,
    isThermalFrame: studyFlags.isThermalFrame,
    isThermalTruss2d: studyFlags.isThermalTruss2d,
    isThermalTruss3d: studyFlags.isThermalTruss3d,
    isSpring1d: studyFlags.isSpring1d,
    isSpring2d: studyFlags.isSpring2d,
    isSpring3d: studyFlags.isSpring3d,
    isFrame: studyFlags.isFrame,
    isPlane: studyFlags.isPlane,
    isTruss: studyFlags.isTruss,
    isBeam: studyFlags.isBeam,
    isTorsion: studyFlags.isTorsion,
    isSpring: studyFlags.isSpring,
    isAxialResult,
    isHeatBar1dResult,
    isHeatPlaneTriangle2dResult,
    isHeatPlaneQuad2dResult,
    isThermalBar1dResult,
    isThermalBeam1dResult,
    isThermalFrame2dResult,
    isThermalTruss2dResult,
    isThermalTruss3dResult,
    isSpring1dResult,
    isSpring2dResult,
    isSpring3dResult,
    isTrussResult,
    isTruss3dResult,
    isBeam1dResult,
    isTorsion1dResult,
    isFrame2dResult,
    isPlaneResult,
  });

  const activeLineResultField =
    studyFlags.isHeatBar
      ? "average_temperature_delta"
      : studyFlags.isSpring ||
        studyFlags.isThermalBar ||
        studyFlags.isThermalTruss2d ||
        studyFlags.isThermalTruss3d
        ? "axial_stress"
        : studyFlags.isBeam
          ? props.beamResultField
          : props.frameResultField;

  const resultDerived = buildWorkbenchResultDerivedData({
    t: props.t,
    isHeatPlane: studyFlags.isHeatPlane,
    isSpring: studyFlags.isSpring,
    isThermalBar: studyFlags.isThermalBar,
    isThermalTruss2d: studyFlags.isThermalTruss2d,
    isThermalTruss3d: studyFlags.isThermalTruss3d,
    isBeam: studyFlags.isBeam,
    isTorsion: studyFlags.isTorsion,
    isFrameLike: studyFlags.isFrameLike,
    isThermalFrame: studyFlags.isThermalFrame,
    isThermalBeam: studyFlags.isThermalBeam,
    planeElements: activeResultState.planeElements,
    planeResultField: props.planeResultField,
    displayTrussElements: activeResultState.displayTrussElements,
    activeLineResultField,
    selectedElement: props.selectedElement,
    planeHotspotLimit: props.planeHotspotLimit,
    selectedNode: props.selectedNode,
    displayTrussNodes: activeResultState.displayTrussNodes,
    displayTruss3dNodes: activeResultState.displayTruss3dNodes,
    displayTruss3dElements: activeResultState.displayTruss3dElements,
    planeNodes: activeResultState.planeNodes,
    thermalFrameModel: props.thermalFrameModel,
    thermalFrameResult: activeResultState.thermalFrameResult,
    frameModel: props.frameModel,
    frameResult: activeResultState.frameResult,
    thermalBeamModel: props.thermalBeamModel,
    thermalBeamResult: activeResultState.thermalBeamResult,
    beamModel: props.beamModel,
    beamResult: activeResultState.beamResult,
    torsionModel: props.torsionModel,
    torsionResult: activeResultState.torsionResult,
    isHeatBar: studyFlags.isHeatBar,
    heatBarModel: props.heatBarModel,
    heatBarResult: activeResultState.heatBarResult,
    thermalBarModel: props.thermalBarModel,
    thermalBarResult: activeResultState.thermalBarResult,
    activeSpringModel: activeResultState.activeSpringModel,
    trussDiagnostics: activeResultState.trussDiagnostics,
    language: props.language,
  });

  const resultExportEffects = {
    result: props.result,
    studyKind: props.studyKind,
    loadedModelName: props.loadedModelName,
    job: props.job,
    isPlane: studyFlags.isPlane,
    isFrameLike: studyFlags.isFrameLike,
    isBeam: studyFlags.isBeam,
    isSpring: studyFlags.isSpring,
    isThermal: studyFlags.isThermal,
    isThermalBar: studyFlags.isThermalBar,
    isThermalTruss2d: studyFlags.isThermalTruss2d,
    isThermalTruss3d: studyFlags.isThermalTruss3d,
    planeResultField: props.planeResultField,
    activeLineResultField,
    planeHotspotElements: resultDerived.planeHotspotElements,
    frameHotspotElements: resultDerived.frameHotspotElements,
    frameForceRows: resultDerived.frameForceRows,
    downloadTextFile: props.downloadTextFile,
    setMessage: props.setMessage,
    labels: {
      noResultToExport: props.t.noResultToExport,
      resultJsonDownloaded: props.t.resultJsonDownloaded,
      resultCsvDownloaded: props.t.resultCsvDownloaded,
    },
  };

  const thermalFrameMaxTemperatureDelta = studyFlags.isThermalFrame
    ? activeResultState.thermalFrameResult?.max_temperature_delta ?? 0
    : 0;
  const thermalFrameMaxTemperatureGradient = studyFlags.isThermalFrame
    ? activeResultState.thermalFrameResult?.max_temperature_gradient ?? 0
    : 0;
  const thermalBeamMaxTemperatureGradient = studyFlags.isThermalBeam
    ? activeResultState.thermalBeamResult?.max_temperature_gradient ?? 0
    : 0;

  const heartbeatStatusValue = heartbeatStatus(props.job, props.t);
  const heartbeatToneValue = heartbeatTone(props.job);
  const translatedFailureReason = humanizeSolverFailure(props.job?.message, props.t);

  const currentMaterials = resolveWorkbenchCurrentMaterials({
    studyKind: props.studyKind,
    planeModel: props.planeModel,
    beamModel: props.beamModel,
    frameModel: props.frameModel,
    thermalBeamModel: props.thermalBeamModel,
    thermalFrameModel: props.thermalFrameModel,
    thermalTrussModel: props.thermalTrussModel,
    thermalTruss3dModel: props.thermalTruss3dModel,
    trussModel: props.trussModel,
    truss3dModel: props.truss3dModel,
  });

  const sidebarDerived = buildWorkbenchSidebarDerived({
    t: props.t,
    language: props.language,
    health: props.health,
    frontendRuntimeMode: props.frontendRuntimeMode,
    directMeshSelectionMode: props.directMeshSelectionMode,
    directMeshExecution: props.directMeshExecution,
    directMeshApiToken: props.directMeshApiToken,
    protocolAgents: props.protocolAgents,
    securityUi: resultDerived.securityUi,
    currentMaterials,
    hiddenMaterials: props.hiddenMaterials,
    studyKind: props.studyKind,
    deferredJobHistory: props.deferredJobHistory,
    deferredResultRecords: props.deferredResultRecords,
    deferredProjectModels: props.deferredProjectModels,
    deferredModelVersions: props.deferredModelVersions,
    adminFilterProjectId: props.adminFilterProjectId,
    adminFilterModelVersionId: props.adminFilterModelVersionId,
    jobHistory: props.jobHistory,
    projects: props.projects,
    securityEventRecords: props.securityEventRecords,
    securityEventWindowFilter: props.securityEventWindowFilter,
    formatTime: props.formatTime,
    formatMilliseconds: props.formatMilliseconds,
    round: props.round,
    heartbeatTone,
    heartbeatStatus,
    humanizeSolverFailure,
  });

  const visualDerived = useWorkbenchVisualDerived({
    studyKind: props.studyKind,
    isAxial: studyFlags.isAxial,
    isBeam: studyFlags.isBeam,
    isFrameLike: studyFlags.isFrameLike,
    isHeatBar: studyFlags.isHeatBar,
    isHeatPlane: studyFlags.isHeatPlane,
    isHeatPlaneTriangle: studyFlags.isHeatPlaneTriangle,
    isSpring: studyFlags.isSpring,
    isSpring3d: studyFlags.isSpring3d,
    isThermalBar: studyFlags.isThermalBar,
    isThermalTruss2d: studyFlags.isThermalTruss2d,
    isThermalTruss3d: studyFlags.isThermalTruss3d,
    isTorsion: studyFlags.isTorsion,
    isTruss: studyFlags.isTruss,
    isTruss3d: studyFlags.isTruss3d,
    activeResultWindow: activeResultState.activeResultWindow,
    activeResultWindowLimitBase: props.resultWindowLimit,
    resultWindowMaxTotal: props.resultWindowMaxTotal,
    resultWindowOffset: props.resultWindowOffset,
    directMeshEndpointsText: props.directMeshEndpointsText,
    immersiveViewport: props.immersiveViewport,
    immersiveToolDrawerOpen: props.immersiveToolDrawerOpen,
    immersiveHelpDrawerOpen: props.immersiveHelpDrawerOpen,
    showShortcutHints: props.showShortcutHints,
    activeLineResultField,
    frameResultFieldMax: resultDerived.frameResultFieldMax,
    currentMaterials,
    materialColorMap: sidebarDerived.materialColorMap as Map<string, string>,
    displayTrussElements: activeResultState.displayTrussElements,
    displayTruss3dElements: activeResultState.displayTruss3dElements,
    activePlaneInputModel: activeResultState.activePlaneInputModel,
    planeModel: props.planeModel,
    beamModel: props.beamModel,
    frameModel: props.frameModel,
    thermalBeamModel: props.thermalBeamModel,
    thermalFrameModel: props.thermalFrameModel,
    thermalTrussModel: props.thermalTrussModel,
    thermalTruss3dModel: props.thermalTruss3dModel,
    trussModel: props.trussModel,
    truss3dModel: props.truss3dModel,
    spring3dModel: props.spring3dModel,
    activeSpringModel: activeResultState.activeSpringModel,
    activeBeamLikeModel: activeResultState.activeBeamLikeModel,
    torsionModel: props.torsionModel,
    activeFrameLikeModel: activeResultState.activeFrameLikeModel,
    activeFrameLikeResult: activeResultState.activeFrameLikeResult,
    activeBeamLikeResult: activeResultState.activeBeamLikeResult,
    torsionResult: activeResultState.torsionResult,
    axialResult: activeResultState.axialResult,
    trussResult: activeResultState.trussResult,
    thermalTrussResult: activeResultState.thermalTrussResult,
    thermalTruss3dResult: activeResultState.thermalTruss3dResult,
    truss3dResult: activeResultState.truss3dResult,
    springResult: activeResultState.springResult,
    spring2dResult: activeResultState.spring2dResult,
    spring3dResult: activeResultState.spring3dResult,
    activeSpringResult: activeResultState.activeSpringResult,
    beamResult: activeResultState.beamResult,
    frameResult: activeResultState.frameResult,
    planeResult: activeResultState.planeResult,
    heatBarResult: activeResultState.heatBarResult,
    thermalBarResult: activeResultState.thermalBarResult,
    thermalBeamResult: activeResultState.thermalBeamResult,
    thermalFrameResult: activeResultState.thermalFrameResult,
    axialNodes: activeResultState.axialNodes,
    t: props.t,
  });

  return {
    ...studyFlags,
    ...activeResultState,
    activeLineResultField,
    resultExportEffects,
    thermalFrameMaxTemperatureDelta,
    thermalFrameMaxTemperatureGradient,
    thermalBeamMaxTemperatureGradient,
    heartbeatStatusValue,
    heartbeatToneValue,
    translatedFailureReason,
    currentMaterials,
    ...resultDerived,
    ...sidebarDerived,
    ...visualDerived,
  };
}
