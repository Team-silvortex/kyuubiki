export function applyHubWorkloadsI18n(target) {
    for (const [language, overrides] of Object.entries(HUB_WORKLOADS_I18N)) {
        if (!target[language])
            continue;
        target[language] = {
            ...target[language],
            library: {
                ...target[language].library,
                ...overrides.library,
            },
            bundles: {
                ...target[language].bundles,
                ...overrides.bundles,
            },
            guides: {
                ...target[language].guides,
                ...overrides.guides,
            },
        };
    }
}
