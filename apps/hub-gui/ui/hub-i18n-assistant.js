export function applyHubAssistantI18n(target) {
    for (const [language, overrides] of Object.entries(HUB_ASSISTANT_I18N)) {
        if (!target[language])
            continue;
        target[language] = {
            ...target[language],
            assistant: {
                ...target[language].assistant,
                ...overrides.assistant,
            },
            dynamic: {
                ...target[language].dynamic,
                ...overrides.dynamic,
            },
        };
    }
}
