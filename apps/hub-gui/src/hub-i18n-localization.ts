export function applyHubLocalizationI18n(target: HubI18nRegistry): void {
  for (const [language, overrides] of Object.entries(HUB_LOCALIZATION_I18N)) {
    if (!target[language]) continue;
    target[language] = {
      ...target[language],
      guides: {
        ...target[language].guides,
        ...overrides.guides,
      },
      dynamic: {
        ...target[language].dynamic,
        ...overrides.dynamic,
      },
    };
  }
}
